#ifndef MOTOR_WRAPPER_H
#define MOTOR_WRAPPER_H

class MotorWrapper
{
  public:

    // ************************************************************************
    // MotorWrapper - Constructor 
    // ************************************************************************
    // Description:
    //
    // ************************************************************************
    // Parameters:
    //
    // ************************************************************************
    MotorWrapper(int aPin1,
                 int aPin2, 
                 int aIdleStatePin1,
                 int aIdleStatePin2) :
      mPin1(aPin1),
      mPin2(aPin2),
      mIdleStatePin1(aIdleStatePin1),
      mIdleStatePin2(aIdleStatePin2)
    {
    }

    // ************************************************************************
    //
    // ************************************************************************
    void setup();

    // ************************************************************************
    //
    // ************************************************************************
    void loop();

    // ************************************************************************
    //
    // ************************************************************************
    void set_state_pin1(int aState);

    // ************************************************************************
    //
    // ************************************************************************
    void set_state_pin2(int aState);

    // ************************************************************************
    //
    // ************************************************************************
    void reset();

    // ************************************************************************
    //
    // ************************************************************************
    int get_state_pin1();

    // ************************************************************************
    //
    // ************************************************************************
    int get_state_pin2();

  protected:

  private:
    int     mPin1;
    int     mPin2;
    int     mIdleStatePin1;
    int     mIdleStatePin2;
};
#endif