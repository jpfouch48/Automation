#include "MilliTimer.h"
#include <arduino.h>

// ****************************************************************************
// See header file for details
// ****************************************************************************
MilliTimer::MilliTimer()
{
  reset();
}

// ****************************************************************************
// See header file for details
// ****************************************************************************
unsigned long MilliTimer::delta() const
{
  return (millis() - mStartTime);
}

// ****************************************************************************
// See header file for details
// ****************************************************************************
unsigned long MilliTimer::delta_in_secs() const
{
  return delta() / 1000;
}

// ****************************************************************************
// See header file for details
// ****************************************************************************
void MilliTimer::reset()
{
  mStartTime = millis();
}
