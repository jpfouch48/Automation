#ifndef BALL_VALVE_WRAPPER_H
#define BALL_VALVE_WRAPPER_H

#include <Arduino.h>

class BallValveWrapper
{
  public:
    enum class State
    {
      Idle,
      Open,
      Close,
      Unknown,
    };

    // ************************************************************************
    // BallValveWrapper - Constructor 
    // ************************************************************************
    // Description:
    //
    // ************************************************************************
    // Parameters:
    //
    // ************************************************************************
    BallValveWrapper(int aPin1, int aPin2) :
      mPin1(aPin1),
      mPin2(aPin2),
      mState(State::Unknown)
    {
    }

    // ************************************************************************
    //
    // ************************************************************************
    void setup();

    // ************************************************************************
    //
    // ************************************************************************
    void loop();

    // ************************************************************************
    //
    // ************************************************************************
    void set_open();

    // ************************************************************************
    //
    // ************************************************************************
    void set_close();

    // ************************************************************************
    //
    // ************************************************************************
    void set_idle();

    // ************************************************************************
    //
    // ************************************************************************
    void reset();

    // ************************************************************************
    //
    // ************************************************************************
    State get_state()
    {
      return mState;
    }

  protected:

  private:
    int     mPin1;
    int     mPin2;
    State   mState;
};
#endif